/**
 *
 *
 * @class Animal
 */
class Animal {
    constructor(name) {
        this.name = '';
    }
    /**
     * 动物会飞?
     *
     * @memberof Animal
     */
    fly() {
        console(name + "flying?");
    }

    /**
     *
     *
     * @param {String} lastName
     * @param {String} firstName
     * @memberof Animal
     */
    setName(lastName, firstName) {
        this.lastName = lastName;
        this.firstName = firstName;
    }
}